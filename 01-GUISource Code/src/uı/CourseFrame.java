    private void btnDeleteCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCourseActionPerformed
   // Tablodan seÃ§ilen satÄ±rÄ± kontrol et
    int selectedRow = tblCourses.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a course to delete!");
        return;
    }

    // SeÃ§ilen satÄ±rÄ±n ID deÄerini al
    DefaultTableModel model = (DefaultTableModel) tblCourses.getModel();
    int crsId = (int) model.getValueAt(selectedRow, 0);

    // Course listesinden bu ID'yi sil
    Course.deleteCourse(crsId);

    // Tablodan satÄ±rÄ± kaldÄ±r
    model.removeRow(selectedRow);

    // BaÅarÄ± mesajÄ±
    JOptionPane.showMessageDialog(this, "Course deleted successfully!");
    }//GEN-LAST:event_btnDeleteCourseActionPerformed

